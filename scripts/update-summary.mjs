// ── Git changelog (last 30 commits) ──────────────────────────────────────────
let changelogHtml = "";
try {
  const log = execSync('git log --pretty=format:"%ad|%s" --date=format:"%d.%m.%Y" -30', {
    cwd: join(__dirname, ".."),
  }).toString().trim();

  const entries = {};
  for (const line of log.split("\n")) {
    const [date, ...rest] = line.split("|");
    const msg = rest.join("|").trim();
    if (!entries[date]) entries[date] = [];
    entries[date].push(msg);
  }

  changelogHtml = Object.entries(entries).map(([date, msgs]) => `
    <div class="entry">
      <div class="date">${date}</div>
      ${msgs.map(m => `<p>${m}</p>`).join("")}
    </div>`).join("");
} catch {
  changelogHtml = `<div class="entry"><div class="date">–</div><p>Git log not available.</p></div>`;
}

