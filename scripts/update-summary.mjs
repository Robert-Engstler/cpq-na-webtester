<!-- ── Playwright Test Flow ── -->
<div class="card">
  <h2>🎭 Playwright Test Flow (<code>tests/cpq-na-test.mjs</code>)</h2>
  <p>Runs as a Node.js script in GitHub Actions. One Chromium browser, fresh tab per VIN. Steps are reported to the webhook incrementally as <code>type:step</code> payloads.</p>

  <h3>Phase A — Login (once per run)</h3>
  <div class="step-row"><span class="step-num">1</span><span>Navigate to CPQ URL</span></div>
  <div class="step-row"><span class="step-num">2</span><span>Enter username + password (resolved from <code>CPQ_PASSWORDS</code> JSON secret by Env|Brand|Country key — never passed as plain input to avoid log exposure)</span></div>
  <div class="step-row"><span class="step-num">3</span><span>Submit login form, wait for dashboard</span></div>

  <h3>Phase B — Per-VIN loop</h3>
  <div class="step-row"><span class="step-num">1</span><span><strong>Search machine by VIN</strong> — enter VIN in search field, select result</span></div>
  <div class="step-row"><span class="step-num">2</span><span><strong>Select Genuine Care type</strong> — Annual / Standard / Parts-Only (per-VIN from <code>GC_OPTIONS</code>)</span></div>
  <div class="step-row"><span class="step-num">3</span><span><strong>Select Service Condition</strong> — duration (Annual) or preset (Standard/Parts-Only) from <code>SVC_OPTIONS</code></span></div>
  <div class="step-row"><span class="step-num">4</span><span><strong>Configure specifications</strong> — pre-populated options accepted as-is; un-populated options selected manually (tracked as <code>hadManualSpec: true</code>)</span></div>
  <div class="step-row"><span class="step-num">5</span><span><strong>Save configuration</strong> — clicks Save Config, captures <code>CONFIGxxxxxxxx</code> configuration ID</span></div>
  <div class="step-row"><span class="step-num">6</span><span><strong>Download PDFs</strong> — PartsPicklist + ServiceChecklist per VIN, zipped and uploaded to Vercel Blob</span></div>

  <h3>Phase C — Stage + Order (conditional)</h3>
  <div class="step-row"><span class="step-num">7</span><span><strong>Create quote</strong> — only if <code>ENVIRONMENT=Stage</code> AND <code>STAGE_ENDPOINT=Order</code></span></div>
  <div class="step-row"><span class="step-num">8</span><span><strong>Place order</strong> — captures order ID, stored in <code>order_ids</code> map (VIN → order ID)</span></div>

  <h3>Failure handling</h3>
  <ul>
    <li>On any step failure: screenshot captured, uploaded to Vercel Blob, URL included in step result</li>
    <li>Step result includes: <code>step</code>, <code>passed</code>, <code>url</code>, <code>durationMs</code>, <code>error</code>, <code>screenshotUrl</code>, optional <code>configId</code>/<code>configUrl</code>, <code>hadManualSpec</code>, <code>manualSpecs[]</code></li>
    <li>Each step POSTs to webhook immediately (<code>type:step</code>) so the UI updates in real time</li>
    <li>Final POST includes: <code>type:result</code>, <code>status</code>, full <code>results[]</code>, <code>order_ids</code>, <code>pdf_url</code></li>
    <li>Run can be stopped via <code>POST /api/runs/[id]/stop</code> — sets status to <code>stopped</code></li>
  </ul>

  <h3>Local development mode</h3>
  <p class="note">When run locally (without <code>WEBHOOK_URL</code>/<code>WEBHOOK_SECRET</code>/<code>RUN_ID</code>), the script runs in local mode: test executes normally but results are not posted to the webhook. <code>.env.local</code> is loaded automatically from the repo root.</p>
</div>

